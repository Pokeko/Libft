#include "libft.h"

int	ft_atoi(const char *str)
{
	int resultat;
	int i;
	int neg;

	resultat = 0;
	i = 0;
	neg = 0;
	while ((str[i] >= 7 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
		neg = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		resultat = resultat * 10 + (str[i] - '0');
		i++;
	}
	if (neg)
		return (-resultat);
	return (resultat);
}
